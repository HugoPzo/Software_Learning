import math
import numpy

# Redondeo al minimo
resultado = math.floor(54.5345)
print(resultado)

# Redondeo al maximo
resultado = math.ceil(54.5345)
print(resultado)

# Pi
resultado = math.pi
print(resultado)

# Log
resultado = math.log(125, 5)
print(resultado)

# Log base 10
resultado = math.log10(25)
print(resultado)

# Tangente
resultado = math.tan(534)
print(resultado)

# Coseno
resultado = math.cos(642)
print(resultado)

# Seno
resultado = math.sin(222)
print(resultado)

# Raiz Cuadrada de Pi
resultado = math.sqrt(math.pi)
print(resultado)

# Factorial de 'numero'
resultado = math.factorial(7)
print(resultado)


# Numpy para matematicas avanzadas

resultado = numpy.pow(3, 2)
print(resultado)
