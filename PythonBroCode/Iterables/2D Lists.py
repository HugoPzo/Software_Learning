# Multi Dimensional Lists

drinks = ["coffee", "soda", "tea"]
dinner = ["pizza", "hamburguer", "hotdog"]
dessert = ["cake", "ice cream"]


food = [drinks, dinner, dessert]

print(food)

print("\n")

# Enter to the value of the list
print(food[0])
print(food[1])
print(food[2])

print("\n")

# Enter to the value inside the list inside the list
print(food[0][1])
print(food[1][2])
print(food[2][0])
