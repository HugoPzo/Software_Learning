# Import math library
import math


pi = 3.14

x = 1
y = 2
z = 3


# Redonde
print(round(pi))
# Redondeo superior
print(math.ceil(pi))
# Redondeo inferior
print(math.floor(pi))

# Absolute value
print(abs(pi))

# Potencia 3.14^2
print(pow(pi, 2))

# Raiz cuadrada
print(math.sqrt(pi))

# Retorna el maximo valor de una lista o de variables
print(max(x,y,z))

# Retorna el minimo valor de una lista o de variables
print(min(x,y,z))

